    @Override
    public List<Permission> getAllPermisson() {
        QueryWrapper<Permission> wrapper = new QueryWrapper<>();
        List<Permission> permissionList = baseMapper.selectList(wrapper);

        List<Permission> sortList = sortList(permissionList, "0",0);
        return sortList;
    }


    /**
     * 对集合中的permission进行排序，找到他的父级
     *
     * @param permissionList
     * @return
     */
    public List<Permission> sortList(List<Permission> permissionList, String pid,int level) {

        List<Permission> child = new ArrayList<>();

        for (Permission current : permissionList) {
            String myPid = current.getPid();
            if (myPid .equals(pid)) {
                //说明是pid的子节点
                current.setLevel(level);
                child.add(current);
                List<Permission> permissions = sortList(permissionList, current.getId(),level+1);//自己作为父级，传自己的5
                current.setChildren(permissions);
            }
        }
        return child;
    }


    /**
     * 递归的删除权限以及下面的子权限
     * @param id
     */
    @Override
    public void deletePermissionById(String id) {

       //0.先把id作为pid，查询所有子权限
        List<Permission> permissionList = baseMapper.selectList(new QueryWrapper<Permission>().eq("pid", id));
        //1.遍历子权限，判断子权限下面是否还子权限
        permissionList.forEach(permission -> {
            //将子权限的id作为pid再次调用本方法
            deletePermissionById(permission.getId());
        });

        //2.删除当前权限
        baseMapper.deleteById(id);
    }

    /**
     * 给这个指定的角色分配指定的权限
     * @param roleId
     * @param permissionIdList
     */
    @Override
    public void assignPermissonForRole(String roleId, String[] permissionIdList) {

        //0.将用户id和权限id封装到RolePermission对象，再集体存入数据库

        List<RolePermission> list = new ArrayList<>();
        for (String permissionId : permissionIdList) {

            list.add(new RolePermission(roleId,permissionId));
        }

        rolePermissionService.saveBatch(list);


    }

